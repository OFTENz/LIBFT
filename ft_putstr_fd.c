#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	a;

	if (!s || (fd < 0))
		return ;
	a = 0;
	while (s[a])
	{
		write(fd, &s[a], 1);
		a++;
	}
}
