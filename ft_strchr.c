#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*p;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
		{
			p = (char *)&s[i];
			return (p);
		}
		i++;
	}
	if (s[i] == (char)c)
	{
		p = (char *)&s[i];
		return (p);
	}
	return (NULL);
}
