#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p;
	int		a;

	a = 0;
	p = malloc(sizeof(char) * ft_strlen(s1) + 1);
	if (p == NULL)
		return (NULL);
	while (s1[a])
	{
		p[a] = s1[a];
		a++;
	}
	p[a] = '\0';
	return (p);
}
